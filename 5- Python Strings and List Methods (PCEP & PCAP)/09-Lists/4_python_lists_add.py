
################  Python - Add List Items



######## 1-Append Items

thislist = ["apple", "banana", "cherry"]
thislist.append("orange")
print(thislist)











######## 2-Insert Items

mylist = ["apple", "banana", "cherry"]
mylist.insert(1, "orange")
print(mylist)












######## 3-Extend List

mylist = ["apple", "banana", "cherry"]
tropical = ["mango", "pineapple", "papaya"]
mylist.extend(tropical)
print(mylist)












######## 4-Add Any Iterable

mylist = ["apple", "banana", "cherry"]
mytuple = ("kiwi", "orange")
mylist.extend(mytuple)
print(mylist)









