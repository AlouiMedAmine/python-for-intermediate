##########   Python - Modify Strings

######### 1-Upper Case

a = "Hello, World!"
print(a.upper())

######### 2-Lower Case

a = "Hello, World!"
print(a.lower())

######### 3-Remove Whitespace

a = " Hello, World! "
print(a.strip()) # returns "Hello, World!"

######### 4-Replace String

a = "Hello, World!"
print(a.replace("H", "J"))

######### 5-Split String

a = "Hello, World!"
print(a.split("#")) # returns ['Hello', ' World!']

