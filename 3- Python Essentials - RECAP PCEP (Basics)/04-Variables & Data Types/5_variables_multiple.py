
####   Python Variables-Assign Multiple Values



###### 1-Many Values to Multiple Variables

x, y, z = "Orange", "Banana", "Cherry"
print(x)
print(y)
print(z)


###### 2- One Value to Multiple Variables

x = y = z = "Orange"
print(x)
print(y)
print(z)


###### 3-Unpack a Collection

#Unpack a list:

fruits = {"apple", "banana", "cherry"}
x, y, z = fruits
print(x)
print(y)
print(z)


