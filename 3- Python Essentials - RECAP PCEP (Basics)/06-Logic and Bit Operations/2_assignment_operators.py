###############   Python Assignment Operators

x = 5
print(x)

#Addition
x = 5
x += 3   #the same as x = x + 3
print(x)

#Subtraction
x = 5
x -= 3
print(x)

#Multiplication
x = 5
x *= 3
print(x)

#Division
x = 5
x /= 3
print(x)

#Modulus
x = 5
x%=3
print(x)

#Floor division
x = 5
x//=3
print(x)

#Exponentiation
x = 5
x **= 3
print(x)


#Floor division
#the floor division // rounds the result down to the nearest whole number
